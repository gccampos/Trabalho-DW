package com.trabalhoDW.model;

import java.io.Serializable;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.OneToOne;

/**
 *
 * @author guilherme
 */
@Entity
public class Usuario implements Serializable {

    @Id
    @GeneratedValue
    private int id;
    private String nome;
    private String email;
    private long telefone;
    @OneToOne
    private Esporte esporteFavorito;
    private String endereco;
    private boolean disponivel;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public long getTelefone() {
        return telefone;
    }

    public void setTelefone(long telefone) {
        this.telefone = telefone;
    }

    public Esporte getEsporteFavorito() {
        return esporteFavorito;
    }

    public void setEsporteFavorito(Esporte esporteFavorito) {
        this.esporteFavorito = esporteFavorito;
    }

    public String getEndereco() {
        return endereco;
    }

    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }

    public boolean isDisponivel() {
        return disponivel;
    }

    public void setDisponivel(boolean disponivel) {
        this.disponivel = disponivel;
    }

}
